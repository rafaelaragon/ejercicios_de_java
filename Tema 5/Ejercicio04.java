/**
 * 
 * Crea un programa que muestre los números del 320 al 160 hacia atrás de 5 en 5.
 * Usa for.
 * 
 * @author Rafael Aragón Rodríguez
 */
public class Ejercicio04 {
  public static void main(String[] args) {
    for (int i = 320; i >= 160; i-= 20) {
      System.out.print(i + " ");
    }
  }
}

